package data;

public class ProblemParameters {
    public int truckCapacity;
    public int truckBatteryCapacity;
    public int truckCostPerKm;

    public ProblemParameters(int truckCapacity, int truckBatteryCapacity, int truckCostPerKm) {
        this.truckCapacity = truckCapacity;
        this.truckBatteryCapacity = truckBatteryCapacity;
        this.truckCostPerKm = truckCostPerKm;
    }
}
